class Person():

    def Welcome(self):
        print("Hello Python")
    def add(self,num1,num2):
        print(num1+num2)
    def mul(self,num1,num2):
        print(num1*num2)
    def div(self,num1,num2):
        print(num1/num2)
    def mod(self,num1,num2):
        print(num1%num2)





p=Person()
p.Welcome()
p.div(30,30)
p.mod(90,90)
p.mul(98,39)
p.add(100,100)